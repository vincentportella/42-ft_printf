#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned int	i;
	unsigned char	*s1;

	i = -1;
	s1 = (unsigned char *)s;
	while (++i < n)
	{
		if (s1[i] == (unsigned char)c)
			return ((void *)&s1[i]);
	}
	return (NULL);
}
