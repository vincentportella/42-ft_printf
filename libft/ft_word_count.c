#include "libft.h"

int	ft_word_count(char *s)
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	if (!s)
		return (0);
	while (s[++i])
		if (s[i] != 32 && (s[i + 1] == 32 || s[i + 1] == '\0'))
			j++;
	return (j);
}
